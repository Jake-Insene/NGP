FORMAT RAW AS 'BIN'
ORG 0x00000000

RAM_START = 0x20000000
RAM_END = 0x30000000

main:
	MOV R0, RAM_START & 0xFFFF
	MOVT R0, RAM_START >> 16
	MOV R1, RAM_END & 0xFFFF
	MOVT R1, RAM_END >> 16
	MOV R3, R0

	MOV R2, 0xDCDC
	MOVT R2, 0xDCDC

.loop:
	ST R2, [R0]
	ADD R0, R0, 4
	CMP R0, R1
	BNE .loop
	
	HLT

.zero 0x400000-$