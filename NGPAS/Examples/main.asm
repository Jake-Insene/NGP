FORMAT RAW AS 'ngp'
ORG 0x10000000

; Execution starts at 0x10000000

RAM_START = 0x10000030
RAM_END = 0x20000000

main:
	MOV R0, (RAM_START&0xFFFF)
	MOVT R0, (RAM_START>>16)
	MOV R1, (RAM_END&0xFFFF)
	MOVT R1, (RAM_END>>16)

	MOV R2, #0xFFFF
	MOVT R2, #0xFFFF
	
.LOOP:
	ST R2, [R0]
	ADD R0, R0, #4
	CMP R0, R1
	BNE .LOOP

	HLT
